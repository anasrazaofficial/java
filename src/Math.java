public class Math {
    public Integer sum(Integer num1, Integer num2) {
        return num1 + num2;
    }
    public Integer subtract(Integer num1, Integer num2) {
        return num1 - num2;
    }
    public Integer product(Integer num1, Integer num2) {
        return num1 * num2;
    }
    public Integer divide(Integer num1, Integer num2) {
        return num1 / num2;
    }
}